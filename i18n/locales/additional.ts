export function withAdditional<
  T extends { validation: Record<string, string>; errors: Record<string, string> },
  U extends {
    messages: Record<string, unknown>
    validation: Record<string, string>
    errors: Record<string, string>
  },
>(base: T, extra: U) {
  return {
    ...base,
    ...extra.messages,
    validation: { ...base.validation, ...extra.validation },
    errors: { ...base.errors, ...extra.errors },
  }
}
